#include "pipex.h"

void	space_unreplacer(char ***argv)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while ((*argv)[i])
	{
		while ((*argv)[i][j])
		{
			if ((*argv)[i][j] == '\x01')
				(*argv)[i][j] = ' ';
			j++;
		}
		j = 0;
		i++;
	}
}
